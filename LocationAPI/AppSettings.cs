namespace LocationAPI
{
    /// <summary>
    /// Represents application settings for the IP stack API.
    /// </summary>
    public class AppSettings
    {
        /// <summary>
        /// Gets or sets the API key for the IP stack service.
        /// </summary>
        public string IpStackApiKey { get; set; }

        /// <summary>
        /// Gets or sets the API URL for the IP stack service.
        /// </summary>
        public string IpStackApiUrl { get; set; }
    }
}

